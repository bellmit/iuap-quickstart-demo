public class ActivityProperties {
	
	private String rejectAble;
	
	private String name;
	
	private String extendAttributes;
	
	private String multiInstance;
	
	private String type;

	public String getRejectAble() {
		return rejectAble;
	}

	public void setRejectAble(String rejectAble) {
		this.rejectAble = rejectAble;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getExtendAttributes() {
		return extendAttributes;
	}

	public void setExtendAttributes(String extendAttributes) {
		this.extendAttributes = extendAttributes;
	}

	public String getMultiInstance() {
		return multiInstance;
	}

	public void setMultiInstance(String multiInstance) {
		this.multiInstance = multiInstance;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
